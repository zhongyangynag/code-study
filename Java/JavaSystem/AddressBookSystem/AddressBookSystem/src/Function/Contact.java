package Function;

public class Contact {
	private String Name;
	private String Sex;
	private String PhoneNumber;
	private String Email;
	private String Group;
	private String Remark;
	
	public Contact(String name, String sex, String phoneNumber, String email, String group, String remark) {
		Name = name;
		Sex = sex;
		PhoneNumber = phoneNumber;
		Email = email;
		Group = group;
		Remark = remark;
	}
	

	
	
	
}
